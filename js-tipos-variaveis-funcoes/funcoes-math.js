// Round to integer number more next.
console.log(Math.round(4.3));
console.log(Math.round(3.85));
console.log(Math.round(2.5));

// Ceil (teto) round to value more high.
console.log(Math.ceil(5.2));

// Floor it's oposite to Ceil, round to value more down.
console.log(Math.floor(5.2));

// Trunc round to discard decimal
console.log(Math.trunc(4.3)); // 4
console.log(Math.trunc(4.8)); // 4

// Pow is exponentiation
console.log(Math.pow(4, 2)); // 16
console.log(Math.pow(2.5, 1.5)); //3.952847075210474

// sqrt Raiz quadrada
console.log(Math.sqrt(64)); // 8

// min Return minor value
console.log(Math.min(0, 150, 30, 20, -8, -200));

// max Return max value
console.log(Math.max(0, 150, 30, 20, -8, -200));

// Random return a randomic value
console.log(Math.random()); // 0.6545888464574621
console.log(Math.random()); // 0.6217939646621928
