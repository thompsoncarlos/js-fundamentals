// Calcular o raio de uma circunferência 
// PI * raio * raio

// PI = pode se colocar em maisculo quando é uma variável essencialmente constante.

const PI = 3.141592;
let raio = 10;
let areaCircunferencia = PI * raio * raio;

console.log("O valor da área é: " + areaCircunferencia + "m2.");
console.log(Math.PI); // Method with more precision